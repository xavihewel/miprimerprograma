public class Calculadora {

    public int suma(int num1, int num2) {
        return num1 + num2;
    }

    public int resta(int num1, int num2) {
        return num1 - num2;
    }

    public int multiplicacio(int num1, int num2) {
        return num1 * num2;
    }

    public int divisio(int num1, int num2) {
        return num1 / num2;
    }
}
